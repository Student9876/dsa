bool isBipartite(vector<vector<int>>& adjList, vector<int>& set1, vector<int>& set2) {
    int n = adjList.size();
    vector<int> color(n, -1);

    for (int start = 0; start < n; ++start) {
        if (color[start] == -1) {
            queue<int> q;
            q.push(start);
            color[start] = 0;

            while (!q.empty()) {
                int u = q.front();
                q.pop();

                for (int v : adjList[u]) {
                    if (color[v] == -1) {
                        color[v] = 1 - color[u];
                        q.push(v);
                    } else if (color[v] == color[u]) {
                        return false;
                    }
                }
            }
        }
    }

    for (int i = 0; i < n; ++i) {
        if (color[i] == 0) {
            set1.push_back(i);
        } else {
            set2.push_back(i);
        }
    }

    return true;
}


